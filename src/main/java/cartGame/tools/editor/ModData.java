package cartGame.tools.editor;

public class ModData {
	
	String name;
	
	double version;
	
	public ModData() {
		
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getName() {
		return name;
	}
	
	public void setVersion(double version) {
		this.version = version;
	}
	
	public double getVersion() {
		return version;
	}
}
