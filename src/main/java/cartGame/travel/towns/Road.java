package cartGame.travel.towns;

public class Road {
	
	String ID;
	
	int distance;
	
	public Road() {
		
	}
	
	public String getID() {
		return ID;
	}

	public void setID(String iD) {
		ID = iD;
	}

	public void setDistance(int distance) {
		this.distance = distance;
	}
	
	public int getDistance() {
		return distance;
	}

}
